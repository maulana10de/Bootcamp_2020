let month = date.getMonth() + 1;
if (month == 1) {
  console.log(`Saat ini bulan ke-${month} : Januari`);
} else if (month == 2) {
  console.log(`Saat ini bulan ke-${month} : Februari`);
} else if (month == 3) {
  console.log(`Saat ini bulan ke-${month} : Maret`);
} else if (month == 4) {
  console.log(`Saat ini bulan ke-${month} : April`);
} else if (month == 5) {
  console.log(`Saat ini bulan ke-${month} : Mei`);
} else if (month == 6) {
  console.log(`Saat ini bulan ke-${month} : Juni`);
} else if (month == 7) {
  console.log(`Saat ini bulan ke-${month} : Juli`);
} else if (month == 8) {
  console.log(`Saat ini bulan ke-${month} : Agustus`);
} else if (month == 9) {
  console.log(`Saat ini bulan ke-${month} : September`);
} else if (month == 10) {
  console.log(`Saat ini bulan ke-${month} : Oktober`);
} else if (month == 11) {
  console.log(`Saat ini bulan ke-${month} : November`);
} else {
  console.log(`Saat ini bulan ke-${month} : Desember`);
}
